# ЗАДАЧА 5: Длина неубывающего фрагмента
# ============================================================
def task5():
    n = int(input())
    if n == 0:
        print(0)
        return
    nums = list(map(int, input().split()))
    max_len = 1
    cur_len = 1
    for i in range(1, n):
        if nums[i] >= nums[i - 1]:
            cur_len += 1
            max_len = max(max_len, cur_len)
        else:
            cur_len = 1
    print(max_len)

# Пример входных данных:
# 7
# 1 2 2 3 1 4 5
# Ожидаемый вывод: 4


