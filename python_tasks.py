# ============================================================
# ЗАДАЧА 1: Сумма и среднее для смешанных значений
# ============================================================
def task1():
    n = int(input())
    tokens = input().split()
    total = 0
    for t in tokens:
        if t == 'True':
            total += 1
        elif t == 'False':
            total += 0
        else:
            total += float(t)
    average = total / n
    print(total, f"{average:.6f}")

# Пример входных данных:
# 6
# 10 -3 2.5 True False 41
# Ожидаемый вывод: 13.5 2.250000


