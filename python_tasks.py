# ЗАДАЧА 3: Разворот подстроки по срезу
# ============================================================
def task3():
    s = input()
    i, j = map(int, input().split())
    result = s[:i] + s[i:j][::-1] + s[j:]
    print(result)

# Пример входных данных:
# abcdef
# 2 5
# Ожидаемый вывод: abedcf


