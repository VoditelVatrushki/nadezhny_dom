def uslugi_list(request):
    """
    Каталог услуг.
    Передаём список SERVICES в шаблон через context.
    В шаблоне uslugi.html данные выводятся циклом {% for service in services %}.
    description обрезается до 10 слов фильтром truncatewords:10.
    """
    context = {
        'services': SERVICES,
    }
    return render(request, 'uslugi.html', context)


def uslugi_detail(request, pk):
    """
    Страница конкретной услуги.
    pk — целое число из URL (/uslugi/1/, /uslugi/2/, ..., /uslugi/8/).
    """
    service = next((s for s in SERVICES if s['pk'] == pk), None)
    
    if service is None:
        return render(request, '404.html', status=404)
    
    # Получаем похожие услуги (исключая текущую)
    similar_services = [s for s in SERVICES if s['pk'] != pk][:3]
    
    context = {
        'service': service,
        'similar_services': similar_services,
    }
    return render(request, 'uslugi_detail.html', context)